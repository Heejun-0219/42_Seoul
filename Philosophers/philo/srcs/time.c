#include "../includes/phi.h"

uint64_t	gettime(void)
{
	struct timeval	tv;

	gettimeofday(&tv, NULL);
	return ((tv.tv_sec * (uint64_t)1000) + (tv.tv_usec / 1000));
}

void	pass_the_time(int time, t_state *info)
{
	uint64_t	start;

	start = gettime();
	while (info->died == 0)
	{
		if (gettime() - start >= time)
			break ;
		usleep(10);
	}
}
