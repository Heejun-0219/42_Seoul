int	op_plus(int a, int b)
{
	return (a + b);
}

int	op_minus(int a, int b)
{
	return (a - b);
}

int	op_multi(int a, int b)
{
	return (a * b);
}

int	op_div(int a, int b)
{
	return (a / b);
}

int	op_mod(int a, int b)
{
	return (a % b);
}
