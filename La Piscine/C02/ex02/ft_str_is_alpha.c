int	ft_str_is_alpha(char *str)
{
	while (*str)
	{
		if (*str >= 'a' && *str <= 'z')
			;
		else if (*str >= 'A' && *str <= 'Z')
			;
		else
		{
			return (0);
		}
		++str;
	}
	return (1);
}
